VERIFY_INTER_PROMPT = _VERIFY_COMMON.replace("{SCOPE}", "句间重复 inter_repeat") \
    + "\n\n" + _VERIFY_INTER_BODY

VERIFY_INTRA_PROMPT = _VERIFY_COMMON.replace("{SCOPE}", "句内重复 intra_repeat") \
    + "\n\n" + _VERIFY_INTRA_BODY

