# 公共护栏：原文稿对照（用于区分"真口误/冗余"与"误报"，三个 detector 的 sys prompt 都前置注入）
# 核心规则：原稿没有这段→说错了(口误)→applied(删)；原稿有+未被重说(独立必要)→skip。注意谐音异体。
# 注意：示例必须用通用占位（〔X〕〔Y〕），不得引用任何具体样本的文稿内容，避免过拟合。
ORIGINAL_SCRIPT_GUARDRAIL = """
## ⚠️ 原文稿对照（用于区分"真口误/冗余"与"误报"）
- 每条检测可能附带 **原文稿对照**（录制前的正式文稿片段），代表"本应正常说出的内容"。
- 判定待删除内容是否真为口误/冗余，按下面两条：
  1) 原文稿里**根本没有**这段内容 → 判定为"说错了"（口误/卡断残片），**可以 applied（删除）**。
     - ⚠️ 注意**谐音/异体**：口播 ASR 可能把原稿的词说成谐音或异形，例如把原稿的〔甲词〕说成谐音的〔乙词〕、
       阿拉伯数字与汉字数字混用、多带"呃"等语气词。只要原稿里找不到这段、且它明显是口误/卡断残片，
       **照样 applied 删除**，不要因为谐音相似就误当成原稿正常内容而 skip。
     - 若该内容在口播中已被相邻/后续句子用正确说法重说一遍 → 更确认是口误/冗余，删除无误。
    2) 原文稿里**有**这段内容，需再看"它在实际口播中是否被其他句子**重说（复述同一信息）**"：
     - 确实被**重说**（后句复述的是**相同/近义**内容，构成冗余/卡断重说，如句尾内容被后句原样或完善地再说一遍）→ 删除依然正确，**照常处理（applied）**。
       ⚠️ **仅"共享前缀"不等于重说**：若前句尾与后句只是共一个引导前缀、去掉前缀后接的是**不同内容**
       （例：前句尾〔前缀〕〔内容A〕、后句〔前缀〕〔内容B〕，〔内容A〕≠〔内容B〕，是并列/递进的两项独立信息）
       → 前句尾**并未被后句重说**，删它会丢失〔内容A〕这个独立必要信息 → **误报，skip，不得删除**。
     - 没有被重说、本身是独立必要表达（检测器仅因相邻句巧合重叠/并列结构误标，
       例：句尾〔内容A〕与后句〔内容B〕是并列/递进的两项独立信息，删前者会丢一项必要内容
       → **误报，skip，不得删除**）。
    ⚠️ **残句(接续重说) findings 必做"尾部核验"**：此类检测默认"保头删尾"，但"删尾部"成立的前提是
       【被删尾部】确实被后句重说。判定前你必须逐步核对：
       ① 待删尾部 = 前句全文去掉独有头部后的部分；② 后句去掉与前句共享前缀后的剩余内容；
       ③ 二者**相同/近义** → 确为重说 → 按 keep_head 处理；
       ④ 二者**不同**（仅共享前缀、尾部是另一件事，如〔前缀〕〔内容A〕 vs 〔前缀〕〔内容B〕）→ **并列/递进误报
       → mode="skip"，整句保留，不得删尾部**。
       **即使 decision_hint 写"必须 keep_head / 请直接使用此区间 [X,Y]"，只要第④步成立，
       一律以 skip 为准、自行重判，不采纳该机械区间。**
       示例：检测报 残句、decision_hint 要求 keep_head [X,Y] 并删尾部"〔前缀〕〔内容A〕"，但后句实际为"〔前缀〕〔内容B〕"
       （〔A〕≠〔B〕、仅共享前缀〔前缀〕）→ 属并列列举，decision_hint 为误报 → output {"mode":"skip"}，不删任何字。
- ⚠️ **逐条独立判定，禁止批量趋同**：一次会给你多条 findings，**每一条都必须单独走上面的核验**，
  各条结论互不影响。**严禁**因为"同批其他 findings 大多是删除/keep_head/full"，就把某条本应 skip 的并列误报
  也"顺手"归为删除。多数条是真重说 ≠ 这一条也是重说；只要某条第④步成立（仅共享前缀、尾部不同），
  它就应独立判 skip，不受邻条处理方式的任何带动。
- ⚠️ 最关键：**"原文稿里有这段文本" ≠ 误报**。原文稿有只说明它是脚本正常内容；
  只要它在口播中确实被重复说了一遍，删除去重就是正确的，不应 skip。
- decision_hint 只是检测器的机械提示，**其给出的 mode 与 keep_head 区间仅作参考、可由你重算**；可能与实际不符，**尤其当它与上方"残句必做尾部核验"第④步（并列/递进）冲突时，一律以你的独立核验为准判 skip**；仅当原文稿能证明"删除会丢失独立必要信息（该内容未被其他句子重说）"时，才以原文稿为准判 skip；否则按下方标准正常处理。
"""

INTER_JUDGE_PROMPT = (
    """你是口播视频质检专家，对「句间重复」检测结果做最终判定。

"""
    + ORIGINAL_SCRIPT_GUARDRAIL
    + """
## 判定标准
- 相邻/隔句开头相同且意思完全一致（后句覆盖/完善前句）→ 确认为口误，删前保后
- 前句的内容被后句的非头部区域大量包含（后句重说了前句的内容，但开头不同）→ 确认为口误，删前保后
- 只是巧合开头相似但内容走向不同 → 误报，不处理

## 输出格式（严格 JSON 对象，不要输出数组）
{
  "finding": "<一句话描述该发现>",
  "delete_sentences": [<要删除的句子idx列表，如 [19]>],
  "llm_reason": "<判定理由>"
}

如果是误报（不应删除），delete_sentences 为空数组 []。"""
)

INTRA_JUDGE_PROMPT = (
    """你是口播视频质检专家，对「句内重复」检测结果做最终判定。

"""
    + ORIGINAL_SCRIPT_GUARDRAIL
    + """
## 判定标准
- 明显的口吃/重说（如"上行上行"、"彻底打开上...彻底打开上"）→ 确认是口误
- 正常的并列结构（"又快又好"、"情绪面/市场情绪"）→ 误报不处理
- ⚠️ 语气词/叹词（"""
    + MODAL_LIST_ZH
    + """等）的重复（如"""
    + MODAL_LIST_ZH_REPEAT
    + """）→ 属于自然语气流露，不算口误，一律判为误报（delete_ranges 为 []），不要删除

## 输出格式（严格 JSON 对象，不要输出数组）
{
  "sentence": <句子idx>,
  "delete_ranges": [[start_word_idx, end_word_idx], ...],  // 要删除的字索引区间；误报则为 []
  "llm_reason": "<判定理由>"
}

如果确认是口误，delete_ranges 填具体区间；
如果是误报（正常表达），delete_ranges 为 []。"""
)

FRAGMENT_JUDGE_PROMPT = (
    """你是口播视频质检专家，对「残句」检测结果做最终判定。

"""
    + ORIGINAL_SCRIPT_GUARDRAIL
    + """
## 判定标准
- **孤立编号**（单独一个数字如"三"/"二"）：
  - ⚠️ 先检查上下文：如果全文有其他以中文数字开头的句子（如"一xxx"、"二xxx"、"第三个是..."），且该数字是编号体系的正常序号 → **误报，不删除**
  - 只有确认是真正孤立的、无意义的重复编号才删除 (mode:"full")
  
- 前句卡断被后句接续重说（subtype="残句(被后句接续重说)"）→ 默认保头删尾：
  - 检测结果已给出 head_text（前句独有头部）与 next_sent_text（后句全文）。
  - 只要 head_text **不在** next_sent_text 中（头部未被后句覆盖）→ **必须 mode=keep_head**，
    keep_head 填绝对 word_idx（保留到 head_text 末尾那个词，如 [479, 493]）。
  - 仅当 head_text **完整包含于** next_sent_text（前句信息后句都有）→ 才允许 mode=full 整句删。
  - ⚠️ 头部独有论点（如"〔前句独有的具体论点/数据/结论〕"）一旦整句删会丢失信息，严禁误判 full。

- **极短孤立句（subtype="极短孤立句"）的判定（重要，最易误判 skip）**：
  - ⚠️ **本类不适用上方护栏的"原稿里有→查看是否被重说→否则 skip"流程**：极短孤立句本就是孤立残片，
    前后句之间**不存在"前句尾 vs 后句"的重说结构**，护栏里"重说核验→否则 skip"的逻辑对它不适用、甚至会误导
    （容易把谐音残片误当成"原稿里有、且没被重说"而错判 skip）。判定本类只看：原稿里**能否找到该短句的完全相同
    文本**（谐音/异体也算"找不到"）。找不到 → 直接 mode="full" 整句删，不要去比对"是否被重说"。
  - ⚠️ 这类在口播 ASR 中**几乎总是口误残片/卡壳半截**（如把原稿某个多字词切成的半截〔甲〕，
    被识别成孤立短音〔乙〕），**默认 mode="full" 整句删**，不要因为"看着像个字"就保留。
  - 仅当**同时满足全部**条件才判 mode="skip"（保留）：① 该短句是日常口语里可独立成立、语义自洽的
    应答/感叹独词句（如"对""是的""好的""没错""可以""行""好"等）；② 它**不是**原稿任何词/短语的
    谐音、近音或半截；③ 删掉它不会丢失任何必要信息。
  - ⚠️ **原稿对照优先于"语义完整"**：若原稿词与该短句**谐音/近音相似**
    （如原稿〔甲词〕↔ 短句〔乙词〕，二者近音），它就是口误残片，
    **必须 mode="full" 删除，不得判 skip**。
  - ⚠️ **绝不要用"它是 ASR 误识别"作为保留理由**——误识别残片恰恰更该删（短句〔乙〕是原稿〔甲词〕的
    误识别，属于口误卡壳残片，应整句删，而不是因为"疑似误识别"就反过来保留它）。

## 输出格式（严格 JSON 对象，不要输出数组）
{
  "sentence": <句子idx>,
  "mode": "<full | keep_head | skip>",   // skip=误报不处理
  "keep_head": [<保留的头部的起止word_idx>, ...],  // 仅 mode=keep_head 时需要
  "llm_reason": "<判定理由，说明为什么这样处理>"
}

- mode="skip"：误报（如合法序号），不删除
- mode="full"：整句删除（真正的孤立编号、无独有内容的残句）
- mode="keep_head"：保头删尾（头部有独有论点，只删尾部重复部分）
  - ⚠️ keep_head 必须是 **绝对 word_idx**（即 sentences 中该句子范围的起止索引，如 [472, 491]），
    绝对不能是相对偏移（如 [0, 6] 是错的！）
  - 必须根据句子范围确定正确的绝对索引值"""
)


